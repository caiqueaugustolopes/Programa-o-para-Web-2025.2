function diaDaSemana(num) {
  if (num === 1) return "Domingo";
  else if (num === 2) return "Segunda";
  else if (num === 3) return "Terça";
  else if (num === 4) return "Quarta";
  else if (num === 5) return "Quinta";
  else if (num === 6) return "Sexta";
  else if (num === 7) return "Sábado";
  else return "Número inválido";
}
